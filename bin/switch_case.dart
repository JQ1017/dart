void main() {
  String day = 'Monday';

  switch (day) {
    case 'Monday':
      print('Hari Senin');
      break;
    case 'Tuesday':
      print('Hari Selasa');
      break;
    case 'Wednesday':
      print('Hari Rabu');
      break;
    case 'Thursday':
      print('Hari Kamis');
      break;
    case 'Friday':
      print('Hari Jumat');
      break;
    case 'Saturday':
      print('Hari Sabtu');
      break;
    case 'Sunday':
      print('Hari Minggu');
      break;
    default:
      print('Hari Lainnya');
  }
}
