void main() {
  int num1 = 10;
  int num2 = 5;
  int num3 = 15;
  int num4 = 20;

// +
  var result = num1 + num2;
  print(result);

// -
  var result2 = num1 - num2;
  print(result2);

// *
  var result3 = num1 * num2;
  print(result3);

// /
  var result4 = num1 / num2;
  print(result4);

// random
  var result5 = num1 / num2 - num3 + num1 * num4;
  print(result5);
}
